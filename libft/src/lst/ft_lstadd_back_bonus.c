#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	if (lst == NULL)
		return ;
	tmp = ft_lstlast(*lst);
	if (tmp == NULL)
	{
		*lst = new;
		return ;
	}
	tmp->next = new;
}
